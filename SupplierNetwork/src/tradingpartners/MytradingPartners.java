package tradingpartners;

public class MytradingPartners {

	private String selected;
	private String tradingPartnerName;
	private String tradingPartnerAlias;
	private String contactName;
	private String contactEmail;
	private String messagingStatus;
	private String updated;
	private String removed;
	private String added;
	public String getSelected() {
		return selected;
	}
	public void setSelected(String selected) {
		this.selected = selected;
	}
	public String getTradingPartnerName() {
		return tradingPartnerName;
	}
	public void setTradingPartnerName(String tradingPartnerName) {
		this.tradingPartnerName = tradingPartnerName;
	}
	public String getTradingPartnerAlias() {
		return tradingPartnerAlias;
	}
	public void setTradingPartnerAlias(String tradingPartnerAlias) {
		this.tradingPartnerAlias = tradingPartnerAlias;
	}
	public String getContactName() {
		return contactName;
	}
	public void setContactName(String contactName) {
		this.contactName = contactName;
	}
	public String getContactEmail() {
		return contactEmail;
	}
	public void setContactEmail(String contactEmail) {
		this.contactEmail = contactEmail;
	}
	public String getMessagingStatus() {
		return messagingStatus;
	}
	public void setMessagingStatus(String messagingStatus) {
		this.messagingStatus = messagingStatus;
	}
	public String getUpdated() {
		return updated;
	}
	public void setUpdated(String updated) {
		this.updated = updated;
	}
	public String getRemoved() {
		return removed;
	}
	public void setRemoved(String removed) {
		this.removed = removed;
	}
	public String getAdded() {
		return added;
	}
	public void setAdded(String added) {
		this.added = added;
	}

}
